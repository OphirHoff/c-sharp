using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Print_your_name
{
    internal class Program
    {
        static void Main(string[] args)
        {

             // print name 10 times in one line
             for (int i = 1; i <=10; i++)
             {
                Console.Write(" Ophir");
             }

             // print name 10 times each line per word
             for (int x = 1; x <=10; x++)
             {
                Console.WriteLine("Ophir");
             }

             
        }
    }
}
